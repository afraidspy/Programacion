"""
 TIPOS DE DATOS PRIMITIVOS EN PYTHON
"""

# ---------------------------------------------------------
# PARTE 1: VARIABLES
# ---------------------------------------------------------

print("\nPARTE 1: VARIABLES")

edad = 18
nombre = "Messi"
altura = 1.0
print("La variable 'edad' guarda el valor:", edad)
print("El tipo de dato de 'edad' es:", type(edad))
print("El tipo de dato de 'nombre' es:", type(nombre))
print("El tipo de dato de 'altura' es:", type(altura))


# ---------------------------------------------------------
# PARTE 2: NUMEROS ENTEROS (int)
# ---------------------------------------------------------

print("\nPARTE 2: NUMEROS ENTEROS (int)")

numero1 = 10
numero2 = -5

print("numero1:", numero1)
print("Tipo de numero1:", type(numero1))

print("numero2:", numero2)
print("Tipo de numero2:", type(numero2))


# ---------------------------------------------------------
# PARTE 3: NUMEROS DECIMALES (float)
# ---------------------------------------------------------

print("\nPARTE 3: NUMEROS DECIMALES (float)")

altura = 1.75
peso = 70.5

print("altura:", altura)
print("Tipo de altura:", type(altura))

print("peso:", peso)
print("Tipo de peso:", type(peso))


# ---------------------------------------------------------
# PARTE 4: TEXTO (str)
# ---------------------------------------------------------

print("\nPARTE 4: TEXTO (str)")

nombre = "Maria"
mensaje = "Hola, estoy aprendiendo Python"

print("nombre:", nombre)
print("Tipo de nombre:", type(nombre))

print("mensaje:", mensaje)
print("Tipo de mensaje:", type(mensaje))


# ---------------------------------------------------------
# PARTE 5: BOOLEANOS (bool)
# ---------------------------------------------------------

print("\nPARTE 5: BOOLEANOS (bool)")

es_estudiante = True
le_gusta_programar = False

print("es_estudiante:", es_estudiante)
print("Tipo:", type(es_estudiante))

print("le_gusta_programar:", le_gusta_programar)
print("Tipo:", type(le_gusta_programar))

